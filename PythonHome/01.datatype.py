# 資料: 程式的基本單位
# 數字
3456
3.14
# 字串
"中文"
"Hello World"
# 布林值
True
False
# 有順序，可動的列表 List
[3, 4, 5]
["Hello", "World"]
# 有順序，不可動的列表 Tuple
(3, 4, 5)
("Hello", "World")
# 集合 Set ，沒有順序
{3, 4, 5}
{"Hello", "World"}
# 字典 Dictionary
{"apple":"蘋果", "date":"資料"}
# 變數: 用來儲存資料的自訂名稱
# 變數名稱 = 資料
x = 3
# print(資料)
print(x)
x = True # 取代舊的資料
print(x)